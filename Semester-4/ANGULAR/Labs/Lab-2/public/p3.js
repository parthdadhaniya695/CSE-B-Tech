"use strict";
// 3. WAP to perform String manipulation in TypeScript. (A)
let str = "Hello, I am Dadhaniya Parth";
console.log(`Uppercase: ${str.toUpperCase()}`);
console.log(`Lowercase: ${str.toLowerCase()}`);
console.log(`Lenght: ${str.length}`);
console.log(`Substring(12,21): ${str.substring(11, 21)}`);
console.log(`IndexOf(P): ${str.indexOf("P")}`);
console.log(`Replace(Parth,parth): ${str.replace("Parth", "parth")}`);
